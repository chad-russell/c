keymap.set("n", "<leader>k", ":lua require('nvim-tree.api').tree.toggle { find_file = true, focus = true }<CR>", { desc = "Toggle NvimTree", silent = true })
keymap.set("n", "<leader>?", ":lua require('which-key').show({ global = false })<CR>", { desc = "Show which-key mappings", silent = true })
